#OPERATORS
a= 10
b= 30

#compariton operater
print(a==b)
print(a>b)
print(a!=b)
print(a<b)
print(a>=b)
print(a<=b)

#logical operaters
x=3
y=10

print(x>y and x<y)
print(x>y or x<y)
print(not(x>y))

#membership operater
